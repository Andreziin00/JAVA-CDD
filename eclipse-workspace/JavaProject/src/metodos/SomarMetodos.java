package metodos;

public class SomarMetodos {

	int somar(int a, int b) {
		return a + b;
	}

	int somar(int a, int b, int c) {
		return a + b + c;
	}
	
	int sub(int a, int b) {
		return a - b;
	}

	int sub(int a, int b, int c) {
		return a - b - c;
	}
	
	int mult(int a, int b) {
		return a + b;
	}

	int mult(int a, int b, int c) {
		return a * b * c;
	}
}
